//Print numbers from 10 down to 1 in reverse order. 
public class Q_2 {
    public static void main(String[] args) {
        int i = 10; // start at 10
        while (i >= 1) { // condition: keep going until 1
            System.out.println(i); // print the number
            i--; // decrement
        }
    }
}
